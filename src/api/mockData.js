// This function simulates creating a seat map for a venue.
// In a real application, this data would come from the backend for each specific concert hall.
export const generateSeatMap = () => {
    const rows = ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I', 'J'];
    const seatsPerRow = 16;
    let seatMap = [];
    rows.forEach(row => {
        let rowSeats = [];
        for (let i = 1; i <= seatsPerRow; i++) {
            rowSeats.push({
                id: `${row}${i}`,
                // Simulate that ~20% of seats are already taken
                isAvailable: Math.random() > 0.2,
            });
        }
        seatMap.push({ row, seats: rowSeats });
    });
    return seatMap;
};
